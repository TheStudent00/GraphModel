    """
    The Macro-Architectural Orchestrator (Educational Loop Implementation).

    Demonstrates the explicit physical routing and aggregation math of the NAS.
    Uses basic Python loops to clearly illustrate how the SpectralTensors
    are piped through the sub-nodes and how inelastic collisions (ELEM_SUM)
    conserve mass across parallel dimensions.
    """
