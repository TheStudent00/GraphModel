class Compound(nn.Module):
    """
    The Macro-Architectural Orchestrator (Fully Vectorized).

    Executes deep sequential pipelines or wide parallel explorations.
    Parallel aggregations are fully vectorized using tensor stacking and
    hardware-native reductions, eliminating all Python-level accumulation loops.
    """
    def __init__(self, config, sub_nodes, mode: CompoundMode, op: CompoundOp = None):
        super().__init__()
        self.mode = mode
        self.op = op
        self.sub_nodes = nn.ModuleList(sub_nodes)

        self.exit_norm = _SpectralNorm(config.embed_dim)

    def forward(self, x_q, x_k, x_v):
        """Standard Python control flow. Safely compiled by TorchDynamo."""
        if self.mode == CompoundMode.SERIES:
            out = self._forward_series(x_q, x_k, x_v)
        elif self.mode == CompoundMode.PARALLEL:
            out = self._forward_parallel(x_q, x_k, x_v)
        else:
            raise ValueError(f"Unknown Routing Topology: {self.mode}")

        return self.exit_norm(out)

    def _forward_series(self, x_q, x_k, x_v):
        """Deep Pipeline (Sequential Dispatch)."""
        current_q, current_k, current_v = x_q, x_k, x_v

        for node in self.sub_nodes:
            output_stack = node(current_q, current_k, current_v)
            current_q = current_k = current_v = output_stack

        return current_q

    def _forward_parallel(self, x_q, x_k, x_v):
        """Wide Pipeline (Concurrent Dispatch & Vectorized Aggregation)."""
        branch_outputs = [node(x_q, x_k, x_v) for node in self.sub_nodes]

        if self.op == CompoundOp.ELEM_SUM:
            return self._op_elem_sum(branch_outputs)
        elif self.op == CompoundOp.ELEM_PROD:
            return self._op_elem_prod(branch_outputs)
        elif self.op == CompoundOp.CONCAT:
            return self._op_concat(branch_outputs)
        else:
            raise NotImplementedError(f"Operation {self.op} not yet implemented.")

    # ==========================================
    # VECTORIZED AGGREGATION PHYSICS
    # ==========================================
    def _op_elem_sum(self, branches):
        """
        Vectorized Inelastic Collision.
        Calculates mass-weighted superposition without python loops.
        """
        # 1. Stack all branches along a new temporary dimension (dim=0)
        c_stack = torch.stack([b.coeffs for b in branches], dim=0)
        m_stack = torch.stack([b.meta for b in branches], dim=0)

        # 2. Calculate momentum and sum across the new branch dimension
        total_momentum = torch.sum(c_stack * m_stack, dim=0)
        total_mass = torch.sum(m_stack, dim=0)

        epsilon = 1e-9
        fused_coeffs = total_momentum / (total_mass + epsilon)

        return SpectralTensor(fused_coeffs, total_mass)

    def _op_elem_prod(self, branches):
        """
        Vectorized Gating Filter.
        Executes pointwise multiplication across all branches instantly.
        """
        c_stack = torch.stack([b.coeffs for b in branches], dim=0)
        m_stack = torch.stack([b.meta for b in branches], dim=0)

        # 3. Product across the branch dimension
        fused_coeffs = torch.prod(c_stack, dim=0)
        fused_meta = torch.prod(m_stack, dim=0)

        return SpectralTensor(fused_coeffs, fused_meta)

    def _op_concat(self, branches):
        """
        Vectorized Bandwidth Expansion.
        Glues tensors along the specific Channel dimension (dim=3).
        """
        c_cat = torch.cat([b.coeffs for b in branches], dim=3)
        m_cat = torch.cat([b.meta for b in branches], dim=3)

        return SpectralTensor(c_cat, m_cat)

