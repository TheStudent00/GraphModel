    # ==========================================
    # ROUTING TOPOLOGIES
    # ==========================================
    def _forward_series(self, x_q, x_k, x_v):
        """
        Executes sequential deep processing.
        The output of Node N becomes the exact Q, K, and V input for Node N+1.
        """
        current_q, current_k, current_v = x_q, x_k, x_v

        for node in self.sub_nodes:
            # The node (Atom or nested Compound) processes the current state
            output_stack = node(current_q, current_k, current_v)

            # The newly infused, attended output completely overwrites the
            # routing tensors for the next node in the sequence.
            current_q = output_stack
            current_k = output_stack
            current_v = output_stack

        return current_q

    def _forward_parallel(self, x_q, x_k, x_v):
        """
        Executes concurrent wide processing.
        The exact same inputs are broadcast to every node simultaneously.
        """
        branch_outputs = []

        # Explicitly collect the independent realities
        for node in self.sub_nodes:
            branch_outputs.append(node(x_q, x_k, x_v))

        # Reconcile the geometries based on the GraphModel's mandated operator
        if self.op == CompoundOp.ELEM_SUM:
            return self._op_elem_sum(branch_outputs)
        elif self.op == CompoundOp.ELEM_PROD:
            return self._op_elem_prod(branch_outputs)
        elif self.op == CompoundOp.CONCAT:
            return self._op_concat(branch_outputs)
        else:
            raise NotImplementedError(f"Operation {self.op} not yet implemented.")

    # ==========================================
    # AGGREGATION PHYSICS
    # ==========================================
    def _op_elem_sum(self, branches):
        """
        The Inelastic Collision.
        Superposes multiple branches while strictly conserving the absolute magnitude
        recorded in the `meta` mass tensors.
        """
        base_branch = branches[0]

        # Start accumulators
        total_momentum = base_branch.coeffs * base_branch.meta
        total_mass = base_branch.meta.clone()

        # Add subsequent branches
        for i in range(1, len(branches)):
            branch = branches[i]
            total_momentum += (branch.coeffs * branch.meta)
            total_mass += branch.meta

        # A small epsilon prevents division by zero in perfect vacuums
        epsilon = 1e-9

        # Resolve the final wave state: (P1 + P2 + ...) / (M1 + M2 + ...)
        fused_coeffs = total_momentum / (total_mass + epsilon)
        fused_meta = total_mass

        return SpectralTensor(fused_coeffs, fused_meta)

    def _op_elem_prod(self, branches):
        """
        The Gating Filter.
        Multiplies branches element-wise. Branch A can act as a frequency
        filter for Branch B.
        """
        fused_coeffs = branches[0].coeffs
        fused_meta = branches[0].meta

        for i in range(1, len(branches)):
            # Continuous polynomial convolution (pointwise)
            fused_coeffs = fused_coeffs * branches[i].coeffs
            # Mass scaling (filtering dampens or amplifies total mass)
            fused_meta = fused_meta * branches[i].meta

        return SpectralTensor(fused_coeffs, fused_meta)

    def _op_concat(self, branches):
        """
        Bandwidth Expansion.
        Glues the independent tensors together along the Channel dimension (dim=3).
        """
        # Assuming shape is [Batch, Sections, Sequence, Channels, ...]
        c_cat = torch.cat([b.coeffs for b in branches], dim=3)
        m_cat = torch.cat([b.meta for b in branches], dim=3)

        return SpectralTensor(c_cat, m_cat)

