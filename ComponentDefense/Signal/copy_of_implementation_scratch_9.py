        super().__init__()
        self.mode = mode
        self.op = op
        self.sub_nodes = nn.ModuleList(sub_nodes)

        # Applies continuous stabilization before handing off to the next macro-node
        self.exit_norm = _SpectralNorm(config.embed_dim)

