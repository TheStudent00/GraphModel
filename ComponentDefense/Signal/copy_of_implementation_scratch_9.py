    def _generate_nd_einsum(self, num_contexts: int) -> str:
        """
        Builds the multilinear contraction string.
        Example (2 Contexts): 'b q c d, b c ..., b d ... -> b q ...'
        """
        chars = 'cdefghij'[:num_contexts]
        grid_str = 'bq' + ''.join(chars)
        payload_strs = ', '.join([f'b{char}...' for char in chars])
        return f'{grid_str}, {payload_strs} -> bq...'

    def _apply_gravitational_cloaking(self, meta: torch.Tensor, theta: torch.Tensor) -> torch.Tensor:
        cloaked_meta = meta.clone()

        cos2 = torch.cos(theta) ** 2
        sin2 = torch.sin(theta) ** 2

        orig_m_real = meta[..., 2]
        orig_m_virt = meta[..., 3]
        orig_z_real = meta[..., 0]
        orig_z_virt = meta[..., 1]

        # Perfect Topological Cross-Rotation (Matches Hahn Decomposition Swap)
        cloaked_meta[..., 2] = orig_m_real * cos2 + orig_m_virt * sin2  # m_real
        cloaked_meta[..., 3] = orig_m_real * sin2 + orig_m_virt * cos2  # m_virt

        cloaked_meta[..., 0] = orig_z_real * cos2 - orig_z_virt * sin2  # z_real
        cloaked_meta[..., 1] = orig_z_real * sin2 + orig_z_virt * cos2  # z_virt

        return cloaked_meta

    def _build_nd_score_grid(self, Q: SpectralTensor, K_list: list[SpectralTensor]) -> torch.Tensor:
        """
        Uses stable Logit Addition across context axes to build a joint probability grid.
        Output Shape: [Batch, N_q, N_k1, N_k2, ...]
        """
        logits_list = []
        for K in K_list:
            QK_tensor = Q @ K.T
            c0_coeffs = QK_tensor.coeffs[..., 0].real
            logits = c0_coeffs.mean(dim=(-1, -2)) * self.scale
            logits_list.append(logits)

        B, N_q = logits_list[0].shape[:2]
        device = logits_list[0].device

        # FIXED: Initialize with trailing ones for proper right-to-left broadcasting
        base_shape = [B, N_q] + [1] * len(K_list)
        nd_logits = torch.zeros(base_shape, device=device)

        for i, logits in enumerate(logits_list):
            view_shape = [B, N_q] + [1] * len(K_list)
            view_shape[2 + i] = logits.shape[2]
            nd_logits = nd_logits + logits.view(*view_shape)

        return nd_logits

    def _apply_nd_softmax(self, nd_logits: torch.Tensor) -> torch.Tensor:
        """Flattens ND space, computes competitive softmax, and reshapes back."""
        original_shape = nd_logits.shape
        B, N_q = original_shape[0], original_shape[1]

        flat_logits = nd_logits.view(B, N_q, -1)
        flat_probs = F.softmax(flat_logits, dim=-1)

        return flat_probs.view(*original_shape)

    def _generate_nd_continuous_identity(self, N_q: int, N_k_list: list[int], device: torch.device) -> torch.Tensor:
        q_starts = torch.arange(N_q, device=device).float() / N_q
        q_ends = torch.arange(1, N_q + 1, device=device).float() / N_q
        q_starts, q_ends = q_starts.unsqueeze(1), q_ends.unsqueeze(1)

        nd_identity = 1.0
        for i, N_k in enumerate(N_k_list):
            k_starts = torch.arange(N_k, device=device).float() / N_k
            k_ends = torch.arange(1, N_k + 1, device=device).float() / N_k
            k_starts, k_ends = k_starts.unsqueeze(0), k_ends.unsqueeze(0)

            overlap_starts = torch.max(q_starts, k_starts)
            overlap_ends = torch.min(q_ends, k_ends)
            overlaps = torch.clamp(overlap_ends - overlap_starts, min=0.0)

            view_shape = [N_q] + [1] * len(N_k_list)
            view_shape[i + 1] = N_k

            # FIXED: Multiply by N_q inside the loop to ensure marginal probabilities sum to 1.0!
            nd_identity = nd_identity * (overlaps.view(*view_shape) * N_q)

        return nd_identity

    def _collapse_payloads(self, blended_scores: torch.Tensor, V_list: list[SpectralTensor]) -> SpectralTensor:
        """
        The Dual-Collapse.
        1. Multi-linear Tensor Contraction for Semantics (coeffs).
        2. Marginalized Mass-Weighted Average for Physics (meta).
        """
        num_contexts = len(V_list)

        # 1. Semantic Collapse (The Hyper-Feature)
        v_coeffs = [V.coeffs for V in V_list]
        output_coeffs = torch.einsum(self.collapse_equation, blended_scores, *v_coeffs)

        # 2. Physics Collapse (The Drift Anchor)
        output_meta = 0.0
        for i, V in enumerate(V_list):
            # Marginalize out all other contexts
            dims_to_sum = tuple(d for d in range(2, 2 + num_contexts) if d != 2 + i)
            marginal_scores = blended_scores.sum(dim=dims_to_sum) if dims_to_sum else blended_scores

            v_meta_contribution = torch.einsum('bqk, bk... -> bq...', marginal_scores, V.meta)
            output_meta = output_meta + v_meta_contribution

        output_meta = output_meta / num_contexts

        return SpectralTensor(output_coeffs, output_meta)

