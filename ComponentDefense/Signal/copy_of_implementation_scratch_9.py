    fig.patch.set_facecolor('#1e1e1e')

    axes[0].axis('off')
    axes[0].imshow(original_np, cmap='gray', vmin=0, vmax=1)
    axes[0].set_title(f"Original Input\nShape: {target_shape}", color='white')

    axes[1].axis('off')
    axes[1].imshow(reconstructed_np, cmap='gray', vmin=0, vmax=1)
    axes[1].set_title(f"Reconstructed Output (Threshold: {config.merge_threshold})\n"
                      f"Tokens: {active_tokens} | Total MSE: {final_mse.item():.6f}", color='white')

    plt.tight_layout()
    plt.show()

