        blended_QK_scores = (self.alpha * normed_QK) + ((1.0 - self.alpha) * Identity)

        # 5. Tensor Contraction (The Dual Collapse)
        output_tensor = self._collapse_payloads(blended_QK_scores, V_list)

        return output_tensor

