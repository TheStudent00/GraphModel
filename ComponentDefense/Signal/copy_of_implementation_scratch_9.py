class SpectralProjector(nn.Module):
    """
    A learned weight matrix existing purely in continuous spectral space.
    Wraps nn.Parameters into a dynamically minted SpectralTensor for math operations.
    """
    def __init__(self, in_dim, out_dim, zero_init=False):
        super().__init__()

        # Dimensions based on your SpectralLayout
        S = 1  # Weights start as single-segment (global) and upsample dynamically
        B = len(SpectralLayout.BOUND_KEYS)
        C = len(SpectralLayout.CHEB_KEYS)
        M = len(SpectralLayout.META_KEYS)

        # Trainable Coefficients [In_Dim, Out_Dim, Segments, Bounds, Cheb]
        self.weight_coeffs = nn.Parameter(torch.randn(in_dim, out_dim, S, B, C) * (in_dim ** -0.5))

        # Trainable Physics Metadata [In_Dim, Out_Dim, Meta]
        self.weight_meta = nn.Parameter(torch.ones(in_dim, out_dim, M))

        if zero_init:
            nn.init.zeros_(self.weight_coeffs)
            # We don't zero meta, because zero mass/length causes physics singularities

    def get_tensor(self):
        """Mints the trainable parameters into a valid SpectralTensor on the fly."""
        return SpectralTensor(self.weight_coeffs, self.weight_meta)

class _AttentionBlock(nn.Module):
    """
        The fundamental cognitive router of the Atom.
        Operates ENTIRELY on continuous SpectralTensors using SpectralAlgebra.
        Supports N-Dimensional Multi-Context Routing and Gravitational Cloaking.
    """
    def __init__(self, embed_dim, num_contexts=1):
        super().__init__()
        self.scale = embed_dim ** -0.5
        self.num_contexts = num_contexts

        # 1. The Gangster Gate
        self.alpha = nn.Parameter(torch.tensor(0.0, dtype=torch.float32))

        # 2. Gravitational Cloaking (Learnable Phase Gates)
        # Initializes at 0 (Fully Real / Max Gravity). Learns to approach pi/2 (Ghost Token).
        self.theta_q = nn.Parameter(torch.tensor(0.0, dtype=torch.float32))
        self.theta_k = nn.Parameter(torch.zeros(num_contexts, dtype=torch.float32))
        self.theta_v = nn.Parameter(torch.zeros(num_contexts, dtype=torch.float32))

        # 3. Native Spectral Projectors (Dynamically sized for ND contexts)
        self.q_proj = SpectralProjector(embed_dim, embed_dim, zero_init=True)
        self.k_projs = nn.ModuleList([
            SpectralProjector(embed_dim, embed_dim, zero_init=True) for _ in range(num_contexts)
        ])
        self.v_projs = nn.ModuleList([
            SpectralProjector(embed_dim, embed_dim, zero_init=False) for _ in range(num_contexts)
        ])

        # 4. Generate the ND C++ Einsum Contraction String once during init
        self.collapse_equation = self._generate_nd_einsum(num_contexts)

    def forward(self, q_stack: SpectralTensor, k_stacks: list[SpectralTensor], v_stacks: list[SpectralTensor]) -> SpectralTensor:
        """
            Inputs:
            - q_stack: The spatial anchor.
            - k_stacks / v_stacks: Lists of contextual inputs (e.g., [Audio, Video, Text]).
        """
        # 1. Spectral Projection & Gravitational Cloaking
        Q = q_stack @ self.q_proj.get_tensor()
        Q.meta = self._apply_gravitational_cloaking(Q.meta, self.theta_q)

        K_list, V_list = [], []
        for i in range(self.num_contexts):
            K = k_stacks[i] @ self.k_projs[i].get_tensor()
            K.meta = self._apply_gravitational_cloaking(K.meta, self.theta_k[i])
            K_list.append(K)

            V = v_stacks[i] @ self.v_projs[i].get_tensor()
            V.meta = self._apply_gravitational_cloaking(V.meta, self.theta_v[i])
            V_list.append(V)

